# Days of the week
user_Number = input('Please enter a number from 1 to 7:')

if user_Number == '1':
    print("monday")
elif user_Number == '2':
    print("tuesday")
elif user_Number == '3':
    print("wednesday")
elif user_Number == '4':
    print("thursday")
elif user_Number == '5':
    print("friday")
elif user_Number == '6':
    print("saturday")
elif user_Number == '7':
    print("sunday")
else:
    print('Error. The number you entered is not a number form 1 to 7 please enter the correct number')
