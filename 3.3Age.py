# Age
User_Age = int(input(" Please enter your age "))
if User_Age <= 1:
    print("You are a an infant")
elif User_Age < 13:
    print("You are a Child")
elif User_Age < 20:
    print("You are a teenager")
elif User_Age >= 20:
    print("You are an adult")
